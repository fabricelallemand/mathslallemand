<h1 id="présentation">Présentation</h1>
<p>Il s'agit d'un travail à réaliser par groupes de 3 à 4 élèves.</p>
<p>Chaque groupe choisi son sujet parmi les propositions qui lui ont été faites.</p>
<p>Le travail demandé doit sera organisé en suivant les étapes suivantes :</p>
<ul>
<li>Recherche documentaire sur le sujet choisi. S'il s'agit d'un jeu, quelle est la règle du jeu précise ? Pour le sujet de robotique, rassembler les documents, mode d'emploi, notices de montage nécessaires. Rechercher aussi les solutions déjà existantes et voir ce qu'il est possible de faire.</li>
<li>Rédiger un cahier des charges du projet. Ce document devra présenter de façon claire et détaillée les fonctionnalités prévues du résultat final. Il pourra être modifié en cours de route en fonction des difficultés rencontrées.</li>
<li>Étudier de façon détaillée une solution existante, afin de comprendre les procédures utilisées, en étant capable d'expliquer des parties du code.</li>
<li>Modifier un programme de base afin d'arriver à une production répondant au cahier des charges prévu.</li>
<li>Présenter un compte rendu oral, sous forme d'exposé, devant l'ensemble de la classe.</li>
</ul>
<p>Cette démarche constitue une bonne introduction à la démarche mise en œuvre lors des TPE en classe de 1ère.</p>
<h1 id="les-sujets-choisis">Les sujets choisis</h1>
<ul>
<li><a href="./robot.php"><strong>Robot et labyrinthe</strong></a>. Un robot est dans un labyrinthe, il s'agit de lui apprendre à en sortir par ses propres moyens. Comment modéliser le labyrinthe, quels capteurs utiliser pour monter le robot ... ? Le robot utilisé sera le robot Lego NXT2. Le langage de programmation utilisé sera le langage par blocs logiques fourni avec le robot.</li>
<li><a href="./tir2d.php"><strong>Jeu de tir en 2 dimensions</strong></a>. Il s'agit de programmer un jeu dans lequel un vaisseau doit évoluer en évitant ou en détruisant des obstacles qui arrivent sur lui. La programmation sera faite en python, à l'aide de la librairie pygame.</li>
<li><a href="./serpent.php"><strong>Jeu du serpent</strong></a>. C'est un classique : un serpent se déplace dans un rectangle et doit manger des aliments qui le font s'agrandir, en évitant de se cogner aux bords du rectangle. La programmation sera faite en python, à l'aide de la librairie pygame.</li>
<li><a href="./puissance4.php"><strong>Jeu de puissance 4</strong></a>. Les élèves de ce groupe ont choisi de programmer ce jeu en HTML5 et Javascript.</li>
</ul>

